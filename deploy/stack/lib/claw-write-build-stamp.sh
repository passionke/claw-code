#!/usr/bin/env bash
# Write deploy/stack/.claw-build-stamp.env after image/host builds. Author: kejiqing
set -euo pipefail
LIB_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PODMAN_DIR="$(cd "${LIB_DIR}/.." && pwd)"
REPO_ROOT="$(cd "${PODMAN_DIR}/../.." && pwd)"
STAMP="${PODMAN_DIR}/.claw-build-stamp.env"
GIT_REV="$(cd "${REPO_ROOT}" && git rev-parse --short HEAD 2>/dev/null || echo unknown)"
GIT_BRANCH="$(cd "${REPO_ROOT}" && git rev-parse --abbrev-ref HEAD 2>/dev/null || echo unknown)"
TS="$(date -u +%Y-%m-%dT%H:%M:%SZ)"
{
  printf '%s\n' '# GENERATED by claw-write-build-stamp.sh — do not edit. kejiqing'
  printf '%s\n' "CLAW_BUILD_GIT_REV=${GIT_REV}"
  printf '%s\n' "CLAW_BUILD_GIT_BRANCH=${GIT_BRANCH}"
  printf '%s\n' "CLAW_BUILD_UTC=${TS}"
  printf '%s\n' 'CLAW_BUILD_FEATURES=claw_pool,gateway_turns_pool_id,pool_registry_host_db,stdout-v1-pool-sse'
} >"${STAMP}"
echo "wrote ${STAMP} (${GIT_BRANCH}@${GIT_REV})"
