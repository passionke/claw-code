# shellcheck shell=bash
# Detect stable CLAW_POOL_ID + CLAW_POOL_ADVERTISE_HOST for host pool-daemon. Author: kejiqing

# Host `claw-pool-daemon` cannot resolve compose service name `postgres`; use published host port.
claw_pool_daemon_database_url() {
  if [[ -n "${CLAW_POOL_DAEMON_DATABASE_URL:-}" ]]; then
    printf '%s' "${CLAW_POOL_DAEMON_DATABASE_URL}"
    return 0
  fi
  local url="${CLAW_GATEWAY_DATABASE_URL:-}"
  if [[ -z "${url}" ]]; then
    return 1
  fi
  local port="${CLAW_GATEWAY_PG_HOST_PORT:-5433}"
  url="${url//@postgres:5432/@127.0.0.1:${port}}"
  url="${url//@postgres:/@127.0.0.1:${port}/}"
  url="${url//@claw-gateway-postgres:5432/@127.0.0.1:${port}}"
  url="${url//@claw-gateway-postgres:/@127.0.0.1:${port}/}"
  printf '%s' "${url}"
}

# Print first non-loopback IPv4 on this host (best-effort).
claw_detect_pool_advertise_host() {
  if [[ -n "${CLAW_POOL_ADVERTISE_HOST:-}" ]]; then
    printf '%s' "${CLAW_POOL_ADVERTISE_HOST}"
    return 0
  fi
  if [[ -n "${CLAW_POOL_ADVERTISE_IP:-}" ]]; then
    printf '%s' "${CLAW_POOL_ADVERTISE_IP}"
    return 0
  fi

  local ip=""
  if [[ "$(uname -s)" == Darwin ]]; then
    local iface
    for iface in en0 en1 en2; do
      ip="$(ipconfig getifaddr "${iface}" 2>/dev/null || true)"
      if [[ -n "${ip}" ]]; then
        printf '%s' "${ip}"
        return 0
      fi
    done
    ip="$(route -n get default 2>/dev/null | awk '/interface: / {print $2}' | head -1)"
    if [[ -n "${ip}" ]]; then
      ip="$(ipconfig getifaddr "${ip}" 2>/dev/null || true)"
      if [[ -n "${ip}" ]]; then
        printf '%s' "${ip}"
        return 0
      fi
    fi
  else
    if command -v hostname >/dev/null 2>&1; then
      ip="$(hostname -I 2>/dev/null | awk '{for (i=1;i<=NF;i++) if ($i !~ /^127\./) {print $i; exit}}')"
      if [[ -n "${ip}" ]]; then
        printf '%s' "${ip}"
        return 0
      fi
    fi
    if command -v ip >/dev/null 2>&1; then
      ip="$(ip -4 route get 1.1.1.1 2>/dev/null | awk '{for (i=1;i<=NF;i++) if ($i=="src") {print $(i+1); exit}}')"
      if [[ -n "${ip}" && "${ip}" != 127.0.0.1* ]]; then
        printf '%s' "${ip}"
        return 0
      fi
    fi
  fi

  local host
  host="$(hostname -s 2>/dev/null || hostname 2>/dev/null || echo pool)"
  host="${host// /-}"
  printf '%s' "${host}"
}

# Stable per machine (not a random uuid per restart).
claw_default_pool_id() {
  if [[ -n "${CLAW_POOL_ID:-}" ]]; then
    printf '%s' "${CLAW_POOL_ID}"
    return 0
  fi
  local host
  host="$(hostname -s 2>/dev/null || hostname 2>/dev/null || echo pool)"
  host="${host// /-}"
  host="${host//[^a-zA-Z0-9._-]/-}"
  printf 'pool-%s' "${host}"
}

# Per-host gateway URL for claw_pool.gateway_base + playground default. Author: kejiqing
claw_sync_host_gateway_urls() {
  local host="${CLAW_POOL_ADVERTISE_HOST:-}"
  [[ -n "${host}" ]] || return 0
  local gw_port="${GATEWAY_HOST_PORT:-18088}"
  export CLAW_POOL_GATEWAY_BASE="http://${host}:${gw_port}"
  case "$(printf '%s' "${CLAW_DEPLOY_PROFILE:-}" | tr '[:upper:]' '[:lower:]')" in
    production)
      export PLAYGROUND_PUBLIC_GATEWAY_BASE="${CLAW_POOL_GATEWAY_BASE}"
      ;;
  esac
}

# Export registry env for pool-daemon and optional gateway.env snippet. Idempotent.
claw_export_pool_registry_env() {
  local script_dir="${1:-}"
  export CLAW_POOL_ADVERTISE_HOST="$(claw_detect_pool_advertise_host)"
  export CLAW_POOL_ID="$(claw_default_pool_id)"
  claw_sync_host_gateway_urls
  if [[ -n "${script_dir}" ]]; then
    mkdir -p "${script_dir}"
    {
      printf '%s\n' '# GENERATED by claw-pool-registry-env.sh — do not edit. kejiqing'
      printf '%s\n' "CLAW_POOL_ADVERTISE_HOST=${CLAW_POOL_ADVERTISE_HOST}"
      printf '%s\n' "CLAW_POOL_ID=${CLAW_POOL_ID}"
    } >"${script_dir}/pool-registry.env"
  fi
}
