#!/usr/bin/env bash
# After primary CI deploy: second gateway+pool on same host, shared PG, then cluster-verify. Author: kejiqing
set -euo pipefail

LIB_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${LIB_DIR}/../../.." && pwd)"
ENV_A="${REPO_ROOT}/.env"
ENV_B="${REPO_ROOT}/.env.ci-node-b"
DRY_RUN=0

for arg in "$@"; do
  case "${arg}" in
    --dry-run) DRY_RUN=1 ;;
    -h | --help)
      echo "usage: ci-cluster-dual-deploy.sh [--dry-run]" >&2
      echo "  Writes ${ENV_B}, starts node B (gateway :18089 + pool :9964), runs cluster-verify." >&2
      echo "  --dry-run  validate + write ${ENV_B} only (no compose/pool/verify)." >&2
      exit 0
      ;;
    *)
      echo "error: unknown arg ${arg} (try --dry-run)" >&2
      exit 1
      ;;
  esac
done

if [[ ! -f "${ENV_A}" ]]; then
  echo "error: run primary deploy first (missing ${ENV_A})" >&2
  exit 1
fi

set -a
# shellcheck disable=SC1090
source "${ENV_A}"
set +a

HOST="${CLAW_POOL_ADVERTISE_HOST:?CLAW_POOL_ADVERTISE_HOST}"
NODE_B_CLUSTER="${CLAW_CI_NODE_B_CLUSTER_ID:-sunmi-ci-02}"
# Single claw-sandbox per node (strict+relaxed workers in one process). Author: kejiqing
POOL_B="${CLAW_CI_NODE_B_POOL_ID:-pool-${NODE_B_CLUSTER}}"
GW_A="${GATEWAY_HOST_PORT:-18088}"
GW_B="${CLAW_CI_NODE_B_GATEWAY_PORT:-18089}"
PG_PORT_A="${CLAW_GATEWAY_PG_HOST_PORT:-5433}"
PG_CTN_A="${CLAW_COMPOSE_PG_CONTAINER:-claw-gateway-postgres}"
# Node B (claw-cib) joins node A PG docker network — DNS is container name, not service "postgres". kejiqing
DB_URL_B="postgres://claw_gateway:clawGw9Dev_Pg@${PG_CTN_A}:5432/claw_gateway"
DB_URL_B_HOST="postgres://claw_gateway:clawGw9Dev_Pg@127.0.0.1:${PG_PORT_A}/claw_gateway"
STRICT_B="${CLAW_CI_NODE_B_STRICT_PORT:-9964}"
PLAY_B="${CLAW_CI_NODE_B_PLAYGROUND_PORT:-18766}"
PODMAN_DIR="$(cd "${LIB_DIR}/.." && pwd)"

# Image pins live in deploy/stack/.claw-image-release.env after `up --release`, not repo .env. kejiqing
# shellcheck disable=SC1091
source "${LIB_DIR}/release-images.sh"
TAG="${CLAW_IMAGE_RELEASE_TAG:-${CLAW_RELEASE_TAG:-}}"
if [[ -n "${TAG}" ]]; then
  claw_apply_release_image_tag "${TAG}"
elif [[ -f "${PODMAN_DIR}/.claw-image-release.env" ]]; then
  set -a
  # shellcheck disable=SC1090
  source "${PODMAN_DIR}/.claw-image-release.env"
  set +a
fi
: "${GATEWAY_IMAGE:?missing GATEWAY_IMAGE — run gateway.sh up --release first}"
: "${CLAW_PODMAN_IMAGE:?missing CLAW_PODMAN_IMAGE}"
GW_IMAGE="${GATEWAY_IMAGE}"
PG_IMAGE="${GATEWAY_PLAYGROUND_IMAGE:-}"
WORKER_IMAGE="${CLAW_PODMAN_IMAGE}"
DOCKER_IMAGE="${CLAW_DOCKER_IMAGE:-${CLAW_PODMAN_IMAGE}}"
RELAXED_IMAGE="${CLAW_RELAXED_PODMAN_IMAGE:-}"

echo "==> CI cluster dual deploy: node A :${GW_A} + node B :${GW_B} (shared PG :${PG_PORT_A} ${PG_CTN_A})" >&2
echo "    node B cluster=${NODE_B_CLUSTER} pool=${POOL_B}" >&2

if [[ "${DRY_RUN}" == 0 ]]; then
  curl -fsS --connect-timeout 5 "http://127.0.0.1:${GW_A}/healthz" >/dev/null \
    || { echo "error: node A gateway not up on :${GW_A}" >&2; exit 1; }
fi

cat >"${ENV_B}" <<EOF
# GENERATED by ci-cluster-dual-deploy.sh — CI second logical node. kejiqing
CLAW_DEPLOY_PROFILE=${CLAW_DEPLOY_PROFILE:-production}
CLAW_CONTAINER_RUNTIME=${CLAW_CONTAINER_RUNTIME:-docker}
CLAW_CLUSTER_ID=${NODE_B_CLUSTER}
CLAW_POOL_ID=${POOL_B}
CLAW_POOL_ADVERTISE_HOST=${HOST}
CLAW_POOL_HTTP_BASE=http://${HOST}:${STRICT_B}
CLAW_POOL_HTTP_PORT=${STRICT_B}
CLAW_STRICT_PODMAN_POOL_SIZE=2
CLAW_STRICT_PODMAN_POOL_MIN_IDLE=1
CLAW_STRICT_DOCKER_POOL_SIZE=2
CLAW_STRICT_DOCKER_POOL_MIN_IDLE=1
CLAW_RELAXED_PODMAN_POOL_SIZE=1
CLAW_RELAXED_PODMAN_POOL_MIN_IDLE=0
CLAW_RELAXED_DOCKER_POOL_SIZE=1
CLAW_RELAXED_DOCKER_POOL_MIN_IDLE=0
CLAW_ALLOW_RELAXED_WORKER=true
CLAW_POOL_HOST_DAEMON=1
CLAW_POOL_DAEMON_SKIP_BUILD=1
CLAW_POOL_DAEMON_USE_SYSTEMD=0

CLAW_GATEWAY_DATABASE_URL=${DB_URL_B}
CLAW_POOL_DAEMON_DATABASE_URL=${DB_URL_B_HOST}
CLAW_GATEWAY_PG_HOST_PORT=${PG_PORT_A}
CLAW_COMPOSE_PG_CONTAINER=${PG_CTN_A}
CLAW_GATEWAY_FILE_LOG=0
CLAW_HOST_LOG_DIR=./deploy/stack/claw-logs-ci-b
CLAW_GATEWAY_SKIP_DB_MIGRATE=1

COMPOSE_PROJECT_NAME=${CLAW_CI_NODE_B_COMPOSE_PROJECT:-claw-cib}
CLAW_COMPOSE_GATEWAY_ONLY=1
CLAW_STACK_INSTANCE=ci-b
CLAW_POOL_RPC_INSTANCE=ci-b
CLAW_GATEWAY_CONTAINER=claw-gateway-rs-ci-b
CLAW_GATEWAY_PLAYGROUND_CONTAINER=claw-gateway-playground-ci-b

GATEWAY_HOST_PORT=${GW_B}
GATEWAY_PLAYGROUND_HOST_PORT=${PLAY_B}
PLAYGROUND_PUBLIC_GATEWAY_BASE=http://${HOST}:${GW_B}
PLAYGROUND_ALLOWED_HOSTS=${HOST},127.0.0.1,localhost,gateway-rs,192.168.9.252,10.200.2.171
PLAYGROUND_ALLOWED_PORTS=${GW_A},${GW_B},8080,8088
CLAW_POOL_GATEWAY_BASE=http://${HOST}:${GW_B}

CLAW_IMAGE_PREFIX=${CLAW_IMAGE_PREFIX:-local}
CLAW_IMAGE_RELEASE_TAG=${TAG}
GATEWAY_IMAGE=${GW_IMAGE}
GATEWAY_PLAYGROUND_IMAGE=${PG_IMAGE}
CLAW_PODMAN_IMAGE=${WORKER_IMAGE}
CLAW_DOCKER_IMAGE=${DOCKER_IMAGE}
CLAW_RELAXED_PODMAN_IMAGE=${RELAXED_IMAGE}

CLAW_AUTO_BOOTSTRAP=0
CLAW_TIMEOUT_SECONDS=${CLAW_TIMEOUT_SECONDS:-1800}
CLAUDE_TAP_MODE=${CLAUDE_TAP_MODE:-docker}
CLAUDE_TAP_IMAGE=${CLAUDE_TAP_IMAGE:-}
CLAW_DOCKER_NETWORK=${CLAW_DOCKER_NETWORK:-claw_default}
EOF

chmod 600 "${ENV_B}" 2>/dev/null || true

# Fail fast if heredoc left unset placeholders (set -u guard for maintainers). kejiqing
set -a
# shellcheck disable=SC1090
source "${ENV_B}"
set +a
: "${CLAW_CLUSTER_ID:?}"
: "${CLAW_POOL_ID:?}"
: "${CLAW_POOL_ID:?}"
: "${GATEWAY_IMAGE:?}"
: "${CLAW_PODMAN_IMAGE:?}"

if [[ "${DRY_RUN}" == 1 ]]; then
  echo "==> dry-run ok: wrote ${ENV_B}" >&2
  exit 0
fi

"${LIB_DIR}/ci-cluster-node-up.sh" "${ENV_B}"

# cluster-verify uses repo .env (node A); probes all online strict gateways from PG.
"${LIB_DIR}/claw-cluster-verify.sh"

echo "==> CI cluster dual deploy + cluster-verify passed" >&2
