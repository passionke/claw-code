#!/usr/bin/env bash
# After primary CI deploy: second gateway+pool on same host, shared PG, then cluster-verify. Author: kejiqing
set -euo pipefail

LIB_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${LIB_DIR}/../../.." && pwd)"
ENV_A="${REPO_ROOT}/.env"
ENV_B="${REPO_ROOT}/.env.ci-node-b"

if [[ ! -f "${ENV_A}" ]]; then
  echo "error: run primary deploy first (missing ${ENV_A})" >&2
  exit 1
fi

set -a
# shellcheck disable=SC1090
source "${ENV_A}"
set +a

HOST="${CLAW_POOL_ADVERTISE_HOST:?CLAW_POOL_ADVERTISE_HOST}"
GW_A="${GATEWAY_HOST_PORT:-18088}"
GW_B="${CLAW_CI_NODE_B_GATEWAY_PORT:-18089}"
PG_B="${CLAW_CI_NODE_B_PG_PORT:-5433}"
STRICT_B="${CLAW_CI_NODE_B_STRICT_PORT:-9964}"
RELAXED_B="${CLAW_CI_NODE_B_RELAXED_PORT:-9965}"
PLAY_B="${CLAW_CI_NODE_B_PLAYGROUND_PORT:-18766}"
POOL_B="${CLAW_CI_NODE_B_POOL_ID:-pool-sunmi-ci-02}"
TAG="${CLAW_IMAGE_RELEASE_TAG:-${CLAW_RELEASE_TAG:-}}"

echo "==> CI cluster dual deploy: node A :${GW_A} + node B :${GW_B} (shared PG)" >&2

curl -fsS --connect-timeout 5 "http://127.0.0.1:${GW_A}/healthz" >/dev/null \
  || { echo "error: node A gateway not up on :${GW_A}" >&2; exit 1; }

cat >"${ENV_B}" <<EOF
# GENERATED by ci-cluster-dual-deploy.sh — CI second logical node. kejiqing
CLAW_DEPLOY_PROFILE=${CLAW_DEPLOY_PROFILE:-production}
CLAW_CONTAINER_RUNTIME=${CLAW_CONTAINER_RUNTIME:-docker}
CLAW_CLUSTER_ID=${CLAW_CI_NODE_B_CLUSTER_ID:-sunmi-ci-02}
CLAW_POOL_ID=${POOL_B}
CLAW_POOL_ADVERTISE_HOST=${HOST}
CLAW_POOL_HTTP_BASE=http://${HOST}:${STRICT_B}
CLAW_STRICT_POOL_HTTP_PORT=${STRICT_B}
CLAW_RELAXED_POOL_HTTP_PORT=${RELAXED_B}
CLAW_STRICT_PODMAN_POOL_SIZE=2
CLAW_STRICT_PODMAN_POOL_MIN_IDLE=1
CLAW_STRICT_DOCKER_POOL_SIZE=2
CLAW_STRICT_DOCKER_POOL_MIN_IDLE=1
CLAW_RELAXED_PODMAN_POOL_SIZE=1
CLAW_RELAXED_PODMAN_POOL_MIN_IDLE=0
CLAW_RELAXED_DOCKER_POOL_SIZE=1
CLAW_RELAXED_DOCKER_POOL_MIN_IDLE=0
CLAW_ALLOW_RELAXED_WORKER=true
CLAW_POOL_HOST_DAEMON=1
CLAW_POOL_DAEMON_SKIP_BUILD=1
CLAW_POOL_DAEMON_USE_SYSTEMD=0

CLAW_GATEWAY_DATABASE_URL=postgres://claw_gateway:clawGw9Dev_Pg@127.0.0.1:${PG_B}/claw_gateway
CLAW_GATEWAY_PG_HOST_PORT=${PG_B}

COMPOSE_PROJECT_NAME=${CLAW_CI_NODE_B_COMPOSE_PROJECT:-claw-cib}
CLAW_COMPOSE_GATEWAY_ONLY=1
CLAW_STACK_INSTANCE=ci-b
CLAW_POOL_RPC_INSTANCE=ci-b
CLAW_GATEWAY_CONTAINER=claw-gateway-rs-ci-b
CLAW_GATEWAY_PLAYGROUND_CONTAINER=claw-gateway-playground-ci-b

GATEWAY_HOST_PORT=${GW_B}
GATEWAY_PLAYGROUND_HOST_PORT=${PLAY_B}
PLAYGROUND_PUBLIC_GATEWAY_BASE=http://${HOST}:${GW_B}
CLAW_POOL_GATEWAY_BASE=http://${HOST}:${GW_B}

CLAW_IMAGE_PREFIX=${CLAW_IMAGE_PREFIX:-local}
CLAW_IMAGE_RELEASE_TAG=${TAG}
GATEWAY_IMAGE=${GATEWAY_IMAGE}
GATEWAY_PLAYGROUND_IMAGE=${GATEWAY_PLAYGROUND_IMAGE:-}
CLAW_PODMAN_IMAGE=${CLAW_PODMAN_IMAGE}
CLAW_DOCKER_IMAGE=${CLAW_DOCKER_IMAGE:-${CLAW_PODMAN_IMAGE}}
CLAW_RELAXED_PODMAN_IMAGE=${CLAW_RELAXED_PODMAN_IMAGE:-}

CLAW_AUTO_BOOTSTRAP=0
CLAW_TIMEOUT_SECONDS=${CLAW_TIMEOUT_SECONDS:-1800}
CLAUDE_TAP_MODE=${CLAUDE_TAP_MODE:-docker}
CLAUDE_TAP_IMAGE=${CLAUDE_TAP_IMAGE:-}
CLAW_DOCKER_NETWORK=${CLAW_DOCKER_NETWORK:-claw_default}
EOF

chmod 600 "${ENV_B}" 2>/dev/null || true

"${LIB_DIR}/ci-cluster-node-up.sh" "${ENV_B}"

# cluster-verify uses repo .env (node A); probes all online strict gateways from PG.
"${LIB_DIR}/claw-cluster-verify.sh"

echo "==> CI cluster dual deploy + cluster-verify passed" >&2
