#!/usr/bin/env bash
# Write repo-root .env from CI-exported variables (GitLab / GitHub Actions secrets & vars).
# No hand-edited .env on the runner host — set keys in CI settings or workflow env.
# Author: kejiqing
set -euo pipefail

LIB_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${LIB_DIR}/../../.." && pwd)"
ENV_FILE="${REPO_ROOT}/.env"

claw_ci_require() {
  local name="$1"
  if [[ -z "${!name:-}" ]]; then
    echo "error: set ${name} in GitLab/GitHub CI variables or job env before render-env-from-ci.sh" >&2
    exit 1
  fi
}

claw_ci_require CLAW_POOL_ADVERTISE_HOST
claw_ci_require CLAW_CLUSTER_ID

claw_ci_var_set() {
  [[ -n "${!1:-}" ]] && printf yes || printf no
}

claw_ci_require_llm_bootstrap_vars() {
  local key base
  key="${CLAW_BOOTSTRAP_LLM_API_KEY:-${OPENAI_API_KEY:-}}"
  base="${CLAW_BOOTSTRAP_LLM_BASE_URL:-${UPSTREAM_OPENAI_BASE_URL:-${OPENAI_BASE_URL:-}}}"
  if [[ -n "${key}" && -n "${base}" ]]; then
    return 0
  fi
  echo "error: deploy requires LLM bootstrap variables in GitLab/GitHub CI settings" >&2
  echo "  required: CLAW_BOOTSTRAP_LLM_API_KEY (masked)" >&2
  echo "  required: CLAW_BOOTSTRAP_LLM_BASE_URL (OpenAI-compatible, include /v1)" >&2
  echo "  optional: CLAW_BOOTSTRAP_LLM_MODEL_NAME (default gpt-4o-mini)" >&2
  echo "  present in this job (yes/no):" >&2
  echo "    CLAW_BOOTSTRAP_LLM_API_KEY=$(claw_ci_var_set CLAW_BOOTSTRAP_LLM_API_KEY) OPENAI_API_KEY=$(claw_ci_var_set OPENAI_API_KEY)" >&2
  echo "    CLAW_BOOTSTRAP_LLM_BASE_URL=$(claw_ci_var_set CLAW_BOOTSTRAP_LLM_BASE_URL) UPSTREAM_OPENAI_BASE_URL=$(claw_ci_var_set UPSTREAM_OPENAI_BASE_URL) OPENAI_BASE_URL=$(claw_ci_var_set OPENAI_BASE_URL)" >&2
  echo "  if all no: add Variables; uncheck Protected; scope *; only API_KEY should be Masked (not BASE_URL)" >&2
  echo "  doc: deploy/stack/docs/gitlab-ci-variables.md or deploy/stack/docs/github-ci-variables.md" >&2
  exit 1
}

if [[ "${CLAW_CI_REQUIRE_LLM_BOOTSTRAP:-0}" == "1" ]]; then
  claw_ci_require_llm_bootstrap_vars
fi

pool_id="${CLAW_POOL_ID:-pool-${CLAW_POOL_ADVERTISE_HOST//./-}}"
tap_image="${CLAUDE_TAP_IMAGE:-crpi-cf9vxpq3n8or17mw.cn-hangzhou.personal.cr.aliyuncs.com/passionke/claw-tap:latest}"
gw_port="${GATEWAY_HOST_PORT:-18088}"
pg_port="${CLAW_GATEWAY_PG_HOST_PORT:-5433}"
profile="${CLAW_DEPLOY_PROFILE:-production}"
image_prefix="${CLAW_IMAGE_PREFIX:-local}"
auto_bootstrap="${CLAW_AUTO_BOOTSTRAP:-1}"
bootstrap_ds="${CLAW_BOOTSTRAP_DS_ID:-1}"

cat >"${ENV_FILE}" <<EOF
# GENERATED by deploy/stack/lib/render-env-from-ci.sh — do not hand-edit on CI runners.
# Author: kejiqing

CLAW_DEPLOY_PROFILE=${profile}
CLAW_CONTAINER_RUNTIME=${CLAW_CONTAINER_RUNTIME:-docker}
CLAW_CLUSTER_ID=${CLAW_CLUSTER_ID}
CLAW_POOL_ID=${pool_id}
CLAW_POOL_ADVERTISE_HOST=${CLAW_POOL_ADVERTISE_HOST}
CLAW_POOL_HTTP_BASE=${CLAW_POOL_HTTP_BASE:-http://${CLAW_POOL_ADVERTISE_HOST}:9944}
CLAW_POOL_HTTP_PORT=9944
CLAW_STRICT_PODMAN_POOL_SIZE=3
CLAW_STRICT_PODMAN_POOL_MIN_IDLE=1
CLAW_STRICT_DOCKER_POOL_SIZE=3
CLAW_STRICT_DOCKER_POOL_MIN_IDLE=1
CLAW_RELAXED_PODMAN_POOL_SIZE=1
CLAW_RELAXED_PODMAN_POOL_MIN_IDLE=0
CLAW_RELAXED_DOCKER_POOL_SIZE=1
CLAW_RELAXED_DOCKER_POOL_MIN_IDLE=0
CLAW_ALLOW_RELAXED_WORKER=true
CLAW_POOL_HOST_DAEMON=${CLAW_POOL_HOST_DAEMON:-1}
CLAW_POOL_DAEMON_SKIP_BUILD=${CLAW_POOL_DAEMON_SKIP_BUILD:-1}
CLAW_POOL_DAEMON_USE_SYSTEMD=${CLAW_POOL_DAEMON_USE_SYSTEMD:-0}

CLAW_IMAGE_PREFIX=${image_prefix}
CLAW_IMAGE_REGISTRY=${CLAW_IMAGE_REGISTRY:-acr}
CLAW_USE_CN_CRATES_MIRROR=${CLAW_USE_CN_CRATES_MIRROR:-1}
CLAW_USE_CN_RUST_MIRROR=${CLAW_USE_CN_RUST_MIRROR:-1}
CONTAINER_BASE_REGISTRY=${CONTAINER_BASE_REGISTRY:-docker.1ms.run}

GATEWAY_HOST_PORT=${gw_port}
GATEWAY_PLAYGROUND_HOST_PORT=${GATEWAY_PLAYGROUND_HOST_PORT:-18765}
PLAYGROUND_PUBLIC_GATEWAY_BASE=http://${CLAW_POOL_ADVERTISE_HOST}:${gw_port}
PLAYGROUND_ALLOWED_HOSTS=${CLAW_POOL_ADVERTISE_HOST},127.0.0.1,localhost,gateway-rs,192.168.9.252,10.200.2.171
PLAYGROUND_ALLOWED_PORTS=18088,18089,8080,8088
CLAW_TIMEOUT_SECONDS=${CLAW_TIMEOUT_SECONDS:-1800}

CLAW_GATEWAY_DATABASE_URL=${CLAW_GATEWAY_DATABASE_URL:-postgres://claw_gateway:clawGw9Dev_Pg@postgres:5432/claw_gateway}
CLAW_GATEWAY_PG_HOST_PORT=${pg_port}

COMPOSE_PROJECT_NAME=${COMPOSE_PROJECT_NAME:-claw}
CLAUDE_TAP_MODE=${CLAUDE_TAP_MODE:-docker}
CLAUDE_TAP_IMAGE=${tap_image}
CLAUDE_TAP_DOCKER_NETWORK=${CLAUDE_TAP_DOCKER_NETWORK:-claw_default}
CLAW_DOCKER_NETWORK=${CLAW_DOCKER_NETWORK:-claw_default}
CLAUDE_TAP_ADMIN_HOST=${CLAUDE_TAP_ADMIN_HOST:-${CLAW_POOL_ADVERTISE_HOST}}
CLAUDE_TAP_PUBLISH_PROXY=${CLAUDE_TAP_PUBLISH_PROXY:-0.0.0.0:8080:8080}
CLAUDE_TAP_PUBLISH_LIVE=${CLAUDE_TAP_PUBLISH_LIVE:-0.0.0.0:3000:3000}

CLAW_AUTO_BOOTSTRAP=${auto_bootstrap}
CLAW_BOOTSTRAP_DS_ID=${bootstrap_ds}
EOF

# Optional LLM bootstrap (set in GitLab CI/CD Variables, masked).
for _k in CLAW_BOOTSTRAP_LLM_API_KEY CLAW_BOOTSTRAP_LLM_BASE_URL CLAW_BOOTSTRAP_LLM_MODEL_NAME CLAW_BOOTSTRAP_LLM_NAME CLAW_BOOTSTRAP_LLM_FORCE \
  OPENAI_API_KEY UPSTREAM_OPENAI_BASE_URL OPENAI_BASE_URL OPENAI_MODEL; do
  if [[ -n "${!_k:-}" ]]; then
    printf '%s=%s\n' "${_k}" "${!_k}" >>"${ENV_FILE}"
  fi
done

# Langfuse OTEL (optional — set LANGFUSE_* in GitLab CI/CD Variables; see docs/langfuse-otel.md).
for _k in CLAW_OTEL_ENABLED CLAW_OTEL_LOG_PROMPTS LANGFUSE_PUBLIC_KEY LANGFUSE_SECRET_KEY LANGFUSE_BASE_URL \
  OTEL_EXPORTER_OTLP_ENDPOINT OTEL_EXPORTER_OTLP_HEADERS; do
  if [[ -n "${!_k:-}" ]]; then
    printf '%s=%s\n' "${_k}" "${!_k}" >>"${ENV_FILE}"
  fi
done
if [[ -n "${LANGFUSE_PUBLIC_KEY:-}" && -n "${LANGFUSE_SECRET_KEY:-}" ]]; then
  if ! grep -q '^CLAW_OTEL_ENABLED=' "${ENV_FILE}"; then
    printf 'CLAW_OTEL_ENABLED=%s\n' "${CLAW_OTEL_ENABLED:-1}" >>"${ENV_FILE}"
  fi
  if ! grep -q '^LANGFUSE_BASE_URL=' "${ENV_FILE}"; then
    printf 'LANGFUSE_BASE_URL=%s\n' "${LANGFUSE_BASE_URL:-http://${CLAW_POOL_ADVERTISE_HOST}:8090}" >>"${ENV_FILE}"
  fi
fi

chmod 600 "${ENV_FILE}" 2>/dev/null || true
_llm_hint=""
if [[ -n "${CLAW_BOOTSTRAP_LLM_API_KEY:-${OPENAI_API_KEY:-}}" ]]; then
  _llm_hint=" llm=bootstrap"
fi
_otel_hint=""
if grep -q '^CLAW_OTEL_ENABLED=1' "${ENV_FILE}" 2>/dev/null \
  && grep -q '^LANGFUSE_PUBLIC_KEY=' "${ENV_FILE}" 2>/dev/null; then
  _otel_hint=" langfuse=on"
fi
echo "==> wrote ${ENV_FILE} (profile=${profile} cluster=${CLAW_CLUSTER_ID} pool=${pool_id} host=${CLAW_POOL_ADVERTISE_HOST}${_llm_hint}${_otel_hint})"
