#!/usr/bin/env bash
# Write repo-root .env from CI-exported variables (GitLab variables / before_script).
# No hand-edited .env on the runner host — set keys in .gitlab-ci.yml or CI/CD settings.
# Author: kejiqing
set -euo pipefail

LIB_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${LIB_DIR}/../../.." && pwd)"
ENV_FILE="${REPO_ROOT}/.env"

claw_ci_require() {
  local name="$1"
  if [[ -z "${!name:-}" ]]; then
    echo "error: set ${name} in GitLab CI variables or job script before render-env-from-ci.sh" >&2
    exit 1
  fi
}

claw_ci_require CLAW_POOL_ADVERTISE_HOST
claw_ci_require CLAW_CLUSTER_ID

pool_id="${CLAW_POOL_ID:-pool-${CLAW_POOL_ADVERTISE_HOST//./-}}"
gw_port="${GATEWAY_HOST_PORT:-18088}"
pg_port="${CLAW_GATEWAY_PG_HOST_PORT:-5433}"
profile="${CLAW_DEPLOY_PROFILE:-production}"
image_prefix="${CLAW_IMAGE_PREFIX:-local}"

cat >"${ENV_FILE}" <<EOF
# GENERATED by deploy/stack/lib/render-env-from-ci.sh — do not hand-edit on CI runners.
# Author: kejiqing

CLAW_DEPLOY_PROFILE=${profile}
CLAW_CONTAINER_RUNTIME=${CLAW_CONTAINER_RUNTIME:-docker}
CLAW_CLUSTER_ID=${CLAW_CLUSTER_ID}
CLAW_POOL_ID=${pool_id}
CLAW_POOL_ADVERTISE_HOST=${CLAW_POOL_ADVERTISE_HOST}
CLAW_POOL_HTTP_BASE=${CLAW_POOL_HTTP_BASE:-http://${CLAW_POOL_ADVERTISE_HOST}:9944}
CLAW_POOL_HOST_DAEMON=${CLAW_POOL_HOST_DAEMON:-1}
CLAW_POOL_DAEMON_SKIP_BUILD=${CLAW_POOL_DAEMON_SKIP_BUILD:-1}

CLAW_IMAGE_PREFIX=${image_prefix}
CLAW_IMAGE_REGISTRY=${CLAW_IMAGE_REGISTRY:-acr}
CLAW_USE_CN_CRATES_MIRROR=${CLAW_USE_CN_CRATES_MIRROR:-1}
CONTAINER_BASE_REGISTRY=${CONTAINER_BASE_REGISTRY:-docker.1ms.run}

GATEWAY_HOST_PORT=${gw_port}
GATEWAY_PLAYGROUND_HOST_PORT=${GATEWAY_PLAYGROUND_HOST_PORT:-18765}
CLAW_TIMEOUT_SECONDS=${CLAW_TIMEOUT_SECONDS:-1800}

CLAW_GATEWAY_DATABASE_URL=${CLAW_GATEWAY_DATABASE_URL:-postgres://claw_gateway:clawGw9Dev_Pg@postgres:5432/claw_gateway}
CLAW_GATEWAY_PG_HOST_PORT=${pg_port}

COMPOSE_PROJECT_NAME=${COMPOSE_PROJECT_NAME:-claw}
CLAUDE_TAP_MODE=${CLAUDE_TAP_MODE:-docker}
CLAUDE_TAP_DOCKER_NETWORK=${CLAUDE_TAP_DOCKER_NETWORK:-claw_default}
CLAW_DOCKER_NETWORK=${CLAW_DOCKER_NETWORK:-claw_default}
CLAUDE_TAP_PUBLISH_PROXY=${CLAUDE_TAP_PUBLISH_PROXY:-0}
CLAUDE_TAP_PUBLISH_LIVE=${CLAUDE_TAP_PUBLISH_LIVE:-0.0.0.0:3000:3000}
EOF

chmod 600 "${ENV_FILE}" 2>/dev/null || true
echo "==> wrote ${ENV_FILE} (profile=${profile} cluster=${CLAW_CLUSTER_ID} pool=${pool_id} host=${CLAW_POOL_ADVERTISE_HOST})"
