#!/usr/bin/env bash
# Regenerate deploy/podman/worker-openai.env from repo-root .env and set
# CLAW_DOCKER_EXTRA_ARGS / CLAW_PODMAN_EXTRA_ARGS for pool workers.
#
# Run on the server (e.g. /home/admin/claw-code):
#   ./deploy/podman/sync-worker-openai-env.sh
# Updates OPENAI_BASE_URL from CLAUDE_TAP_HOST_PORT / CLAUDE_TAP_BIND_HOST when UPSTREAM_* is set,
# appends --add-host host.docker.internal:host-gateway to CLAW_*_EXTRA_ARGS (override with
# CLAW_POOL_WORKER_RUN_EXTRA= in .env to disable). After changing tap ports, optionally:
#   ./deploy/podman/sync-worker-openai-env.sh --restart
#
# Author: kejiqing
set -euo pipefail

export LC_ALL="${LC_ALL:-C.UTF-8}"
export LANG="${LANG:-C.UTF-8}"

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/../.." && pwd)"
PODMAN_DIR="${SCRIPT_DIR}"
ENV_FILE="${REPO_ROOT}/.env"
WORKER_ENV_PATH="${PODMAN_DIR}/worker-openai.env"

die() {
  echo "error: $*" >&2
  exit 1
}

[[ -f "${ENV_FILE}" ]] || die "missing ${ENV_FILE}"

echo "==> refresh claw-code LLM URL vs claude-tap ports (${ENV_FILE})"
python3 "${PODMAN_DIR}/refresh-tap-llm-chain-in-env.py" "${ENV_FILE}"

upsert_env_kv() {
  local key="$1"
  local val="$2"
  python3 - "${ENV_FILE}" "${key}" "${val}" <<'PY'
import re
import sys

path, key, val = sys.argv[1], sys.argv[2], sys.argv[3]


def fmt_line(k: str, v: str) -> str:
    if re.search(r"[\s#'\"]", v) or v.startswith("-"):
        q = "'" + v.replace("'", "'\"'\"'") + "'"
        return f"{k}={q}\n"
    return f"{k}={v}\n"


with open(path, encoding="utf-8") as f:
    lines = f.readlines()
out, seen = [], False
for line in lines:
    if line.startswith(f"{key}="):
        if not seen:
            out.append(fmt_line(key, val))
            seen = True
        continue
    out.append(line)
if not seen:
    out.append(fmt_line(key, val))
with open(path, "w", encoding="utf-8") as f:
    f.writelines(out)
PY
}

echo "==> writing ${WORKER_ENV_PATH} from ${ENV_FILE}"
python3 - "${ENV_FILE}" "${WORKER_ENV_PATH}" <<'PY'
import os
import sys

root_env_path = sys.argv[1]
worker_env_path = sys.argv[2]

ALLOW = (
    "OPENAI_API_KEY",
    "OPENAI_BASE_URL",
    "OPENAI_ORG_ID",
    "OPENAI_PROJECT_ID",
    "ANTHROPIC_API_KEY",
    "ANTHROPIC_AUTH_TOKEN",
    "ANTHROPIC_BASE_URL",
    "XAI_API_KEY",
    "OPENROUTER_API_KEY",
    "CLAW_DEFAULT_MODEL",
    "ANTHROPIC_MODEL",
    "CLAW_OPENAI_FALLBACK_MODEL",
    "CLAW_PREFER_OPENAI_PREFIX",
    "CLAW_DISABLE_ANTHROPIC_ROUTING",
)


def parse_env(text: str) -> dict[str, str]:
    out: dict[str, str] = {}
    for line in text.splitlines():
        s = line.strip()
        if not s or s.startswith("#"):
            continue
        if "=" not in s:
            continue
        k, _, v = s.partition("=")
        k = k.strip()
        v = v.strip()
        if k in ALLOW and v:
            out[k] = v
    return out


text = open(root_env_path, encoding="utf-8").read()
vals = parse_env(text)
if not vals.get("OPENAI_API_KEY"):
    print(
        "error: OPENAI_API_KEY missing or empty in root .env (workers need it)",
        file=sys.stderr,
    )
    sys.exit(1)
if not vals.get("OPENAI_BASE_URL"):
    print("error: OPENAI_BASE_URL missing or empty in root .env", file=sys.stderr)
    sys.exit(1)

lines = [f"{k}={vals[k]}" for k in ALLOW if k in vals]
body = "\n".join(lines) + "\n"
os.makedirs(os.path.dirname(worker_env_path) or ".", exist_ok=True)
with open(worker_env_path, "w", encoding="utf-8") as f:
    f.write(
        "# Generated by deploy/podman/sync-worker-openai-env.sh from repo root .env. "
        "Author: kejiqing\n"
    )
    f.write(body)
os.chmod(worker_env_path, 0o600)
PY

set -a
# shellcheck disable=SC1090
source "${ENV_FILE}"
set +a

WORKER_ENV_ABS="$(python3 -c "import os; print(os.path.abspath(r'${WORKER_ENV_PATH}'))")"
[[ -f "${WORKER_ENV_ABS}" ]] || die "failed to write ${WORKER_ENV_ABS}"
# Linux Docker/Podman pool workers do not get compose `extra_hosts`; OPENAI_BASE_URL often uses
# host.docker.internal to reach claude-tap on the host. Without this, DNS/connect hangs after
# turn_started (no assistant_iteration / http trace). Docker Desktop usually pre-injects the name;
# --add-host ...:host-gateway is idempotent enough for typical Linux engines. Author: kejiqing
POOL_WORKER_RUN_EXTRA="${CLAW_POOL_WORKER_RUN_EXTRA:---add-host host.docker.internal:host-gateway}"
upsert_env_kv "CLAW_DOCKER_EXTRA_ARGS" "--env-file ${WORKER_ENV_ABS} ${POOL_WORKER_RUN_EXTRA}"
upsert_env_kv "CLAW_PODMAN_EXTRA_ARGS" "--env-file ${WORKER_ENV_ABS} ${POOL_WORKER_RUN_EXTRA}"

echo "OK: ${WORKER_ENV_ABS} and CLAW_*_EXTRA_ARGS updated."

if [[ "${1:-}" == "--restart" ]]; then
  echo "==> restarting stack (stop-with-tap / start-with-tap)"
  "${PODMAN_DIR}/stop-with-tap.sh" || true
  pkill -f 'claude-tap.*--tap-no-launch' 2>/dev/null || true
  sleep 0.5
  # shellcheck source=/dev/null
  source "${PODMAN_DIR}/compose-include.sh"
  RT="$(claw_container_runtime_cli)" || die "need docker or podman"
  mapfile -t STALE < <("${RT}" ps -aq --filter "name=claw-" 2>/dev/null || true)
  if [[ "${#STALE[@]}" -gt 0 ]]; then
    "${RT}" rm -f "${STALE[@]}" || true
  fi
  "${PODMAN_DIR}/start-with-tap.sh"
fi
